        $image->move(public_path('images'), $new_name);
        $form_data = array(
            'first_name'       =>   $request->first_name,
            'last_name'        =>   $request->last_name,
            'image'            =>   $new_name
        );


        // Make a image name based on user name and current timestamp
        // $name = str_slug($request->input('first_name')).'_'.time();
        // Define folder path
